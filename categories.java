    private void category_listValueChanged(javax.swing.event.ListSelectionEvent evt)//GEN-FIRST:event_category_listValueChanged
    {//GEN-HEADEREND:event_category_listValueChanged
        // TODO add your handling code here:
        String qt = (String) category_list.getSelectedValue();
        title_area.setText(qt);
        try
          {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/quotes", "root", "2507");
            pstmt = conn.prepareStatement("SELECT quote FROM quotes WHERE quote_type =? ORDER BY RAND() LIMIT 1");
            pstmt.setString(1, category_list.getSelectedValue());
            rs = pstmt.executeQuery();
            if(rs.next())
              {
                String quoteDesc = rs.getString("quote");

                quote_area.setText(quoteDesc);

              }
            else
              {
                quote_area.setText("");
              }
          }
        catch(ClassNotFoundException | SQLException e)
          {
            JOptionPane.showMessageDialog(null, e);
          }
    }//GEN-LAST:event_category_listValueChanged

