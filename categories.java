    private void c_list ()
    {
        try
          {
            DefaultListModel DML = new DefaultListModel();
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/quotes", "root", "2507");
            pstmt = conn.prepareStatement("SELECT DISTINCT quote_type FROM quotes");
            rs = pstmt.executeQuery();
            while(rs.next())
              {
                String name = rs.getString("quote_type");
                DML.addElement(name);
              }
            category_list.setModel(DML);
            conn.close();
          }
        catch(ClassNotFoundException | SQLException e)
          {
            JOptionPane.showMessageDialog(null, e);
          }
    }

